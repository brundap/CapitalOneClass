package com.capitalone.q11a;

public class Q11a {
	private float f1, f2;

	public Q11a(float f1, float f2) {
		super();
		this.f1 = f1;
		this.f2 = f2;
	}

	public float getF1() {
		return f1;
	}

	public void setF1(float f1) {
		this.f1 = f1;
	}

	public float getF2() {
		return f2;
	}

	public void setF2(float f2) {
		this.f2 = f2;
	}

	public float multiply() {
		return f1 * f2;

	}

	public float add() {
		return f1 + f2;

	}

}
