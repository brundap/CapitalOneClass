package question15;

public class Operations implements ArithmeticOps {

    public int addition(int x, int y) {
	return x + y;
    }
	

    public int substraction(int x, int y) {
		
		return x - y;
	}

	public int multiply(int x, int y) {
	
		return x * y ;
	}

	public int division(int x, int y) {

		return x / y;
	}


}
